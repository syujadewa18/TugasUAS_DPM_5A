class mProduct {
    constructor(id, ownerId, title, imageUrl, description, price, imagesList) {
        this.id = id;
        this.ownerId = ownerId;
        this.title = title;
        this.imageUrl = imageUrl;
        this.description = description;
        this.price = price;
        this.imagesList = imagesList;
    }
}

export default mProduct;